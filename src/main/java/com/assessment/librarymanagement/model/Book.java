package com.assessment.librarymanagement.model;

public class Book {
    private String name;
    private String author;

    public String getName() {
        return this.name;
    }
    public String getAuthor() {
        return this.author;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setAuthor(String author) {
        this.author = author;
    }
}
